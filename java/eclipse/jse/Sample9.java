
public class Sample9 {

	//목표: 가장 중요한 기본 정수 타입인 int 변수를 잘 만들자!
	public static void main(String[] args) {
		int var1 = 10;  // 10진수로 입력
		int var2 = 012;	// 8진수로 입력 (이 때, 0으로 시작)
		int var3 = 0xA;// 16진수로 입력 (이 떄, 0x로 시작)
		int var4 = 0b1010; // 2진수로 입력 (이 때 0b로 시작)
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
	}
}

